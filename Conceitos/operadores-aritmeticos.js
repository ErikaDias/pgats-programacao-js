// + Adição
// - Subtração
// * Multiplicação
// / Divisão
// % Resto da divisão
// ** Potenciação/Exponencial
// Precedência de operadores

// Exemplo de operadores aritméticos
console.log(`10 + 5 = ${10 + 5}`); // 15
console.log(`10 - 5 = ${10 - 5}`); // 5
console.log(`10 * 5 = ${10 * 5}`); // 50
console.log(`10 / 5 = ${10 / 5}`); // 2
console.log(`10 % 5 = ${10 % 5}`); // 0
console.log(`10 ** 2 = ${10 ** 2}`); // 100
console.log(`10 ** 3 = ${10 ** 3}`); // 1000
console.log(`10 + 5 * 2 = ${10 + 5 * 2}`); // 20
console.log(`+"5" * 2 = ${+ "5" * 2}`); // 10

console.log(`Teste ${Math.floor(10.5)}`); // Teste 10
console.log(`Teste ${Math.ceil(10.5)}`); // Teste 11
console.log(`Teste ${Math.round(10.5)}`); // Teste 11
